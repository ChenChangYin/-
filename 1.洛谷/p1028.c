#include <stdio.h>

int main() {
	int a[1005], n;
	scanf("%d", &n);
	for(int i = 1; i < 1005; i++) {
		a[0] = 1, a[2] = 2;
		if(i % 2 == 1) {
			a[i] = a[i-1];
		} else {
			a[i] = a[i-1] + a[i/2];
		}
	}
	printf("%d\n", a[n]);
	return 0;
}

