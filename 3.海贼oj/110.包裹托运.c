#include<stdio.h>

int main() {
    double X;
    scanf("%lf", &X);
    if (X <= 15) {
        printf("%.2lf\n", 6 * X);
    } else {
        printf("%.2lf\n", 15 * 6 + (X - 15) * 9);
    }
    return 0;
}
