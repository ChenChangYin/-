#include<stdio.h>

int main() {
    int n, a = 1, b = 1, c = 2;
    scanf("%d", &n);
    while(n - 3 >= 0) {
        c = a + b;
        a = b;
        b = c;
        n--;
    }
    printf("%d\n", c);
    return 0;
}
